'use strict';

// === - equality operator
// !== - equal operator
// < -  less than operator
//  > - greater than operator
// <= - less than or equal to operator
// >= - greater than or equal to opeartor

let temp = 31;
// let isFreezing = true
//
let isFreezing1 = temp === 31;
let isFreezing2 = temp !== 31;
let isFreezing3 = temp < 31;
let isFreezing4 = temp > 31;
let isFreezing5 = temp <= 31;
let isFreezing6 = temp >= 31;

console.log(isFreezing1);
console.log(isFreezing2);
console.log(isFreezing3);
console.log(isFreezing4);
console.log(isFreezing5);
console.log(isFreezing6);
